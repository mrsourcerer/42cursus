#include <stdio.h>
#include <stdlib.h>

int	ft_strlen(char *s)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

char	*ft_create(char *s)
{
	char	*tmp;
	int		i;

	tmp = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	i = 0;
	while (s[i])
	{
		tmp[i] = s[i];
		i++;
	}
	return (tmp);
}

void	ft_modify(char **first, char **second)
{
	char	*tmp;

	tmp = ft_create("Primero nuevo");
	free(*first);
	*first = NULL;
	*first = tmp;
}

void	ft_print(char **s1, char **s2)
{
	size_t	i;

	i = 0;
	printf("s1: %p - *s1: %p - **s1: %c\n", s1, *s1, **s1);
	printf("s1: %p - *s1: %c - **s1: %c\n", s1, *s1[0], **s1);
	printf("s1: %p - *s1: %c - **s1: %c\n", s1, *(*s1 + 1), s1[0][1]);
	printf("s1: %p - *s1: %c - **s1: %c\n", s1, *(*s1 + 1), (*s1)[1]);
	while (i < ft_strlen(*s1) && i < ft_strlen(*s2))
	{
		printf("%c", (*s1)[i]);
		printf("%c\n", (*s2)[i]);
		i++;
	}
}

int	main(void)
{
	char	*first;
	char	*second;

	first = ft_create("Primera frase");
	second = ft_create("Segunda frase");
	printf("%s\n", first);
	printf("%s\n", second);
	ft_modify(&first, &second);
	printf("%s\n", first);
	printf("%s\n", second);
	ft_print(&first, &second);
	return (0);
}
